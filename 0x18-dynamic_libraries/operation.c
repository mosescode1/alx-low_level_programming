#include "main.h"
#include <stdio.h>
/**
 *add - addition of variable a and b
 * @a: first variable
 * @b: second variable
 * Return: Always the value of a and b
 */
int add(int a, int b)
{
return (a + b);
}

/**
 * sub - subtraction of variable a and b
 * @a: first variable
 * @b: second variable
 * Return: Always the value of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - division of variable a and b
 * @a: first variable
 * @b: second variable
 * Return: Always the value of a and b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus of variable a and b
 * @a: first variable
 * @b: second variable
 * Return: Always the value of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - multiplcation  of variable a and b
 * @a: first variable
 * @b: second variable
 * Return: Always the value of a and b
 */
int mul(int a, int b)
{
	return ((a) * (b));
}
